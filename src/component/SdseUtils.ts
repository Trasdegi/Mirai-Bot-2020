let formatContent = (content) => {

    let cleanedContent = content.replace(/<CLT \d\d>/g, "").replace(/<CLT>/g, "");
    const ponctuation = [".", "?", ";", ":", "!"];

    if (cleanedContent.length <= 64) {
        return content;
    }

    content = content.replace(/\n/g, " ");

    let newContent = "";

    let length = 0;
    let inClt = false;
    for (let i = 0 ; i < content.length ; i++) {

        if (content[i] === '<' && content.indexOf('>', i) !== -1) {
            inClt = true;
        }
        if (content[i] === '>') {
            inClt = false;
        }

        if (length === 64) {
            let j = i;

            if (content[i] === ' ') {
                while (content[j] === ' ' && j > 0) {
                    j -= 1;
                }
            }

            if (ponctuation.includes(content[j])) {
                while (content[j] !== ' ' && j > 0) {
                    j -= 1;
                }
                while (content[j] === ' ' && j > 0) {
                    j -= 1;
                }
            }

            while (content[j] !== ' ' && j > 0) {
                j -= 1;
            }
            let end = newContent.slice(j).trimLeft();
            newContent = `${newContent.slice(0, j)}\n${end}`;
            length = end.length;
        }

        newContent += content[i];
        if (!inClt) {
            if (content[i] === "\n") {
                length = 0;
            } else {
                length += 1;
            }
        }
    }

    return newContent;
};

